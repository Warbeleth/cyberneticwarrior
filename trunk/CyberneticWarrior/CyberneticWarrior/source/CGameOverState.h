#ifndef _CGAMEOVERSTATE_H_
#define _CGAMEOVERSTATE_H_

#include "IGameState.h"
#include "CFont.h"

class CSGD_Direct3D;
class CSGD_DirectInput;
class CSGD_TextureManager;
class CSGD_WaveManager;
class CSGD_DirectSound;
class CEvent;

//////////////////////////////////////////////////////////////////////////////////////////////////////
//				 ******************* CLASS - CAchievementsState *******************
//////////////////////////////////////////////////////////////////////////////////////////////////////
class CGameOverState : public IGameState
{
private:
	enum options{CONTINUE = 0, EXIT};
	// Font
	CFont	m_GameOverFont;
		
	int		m_nScrollingID;
	// Texture IDs
	int	m_nBackgroundID;
	int m_nSelection;
	int m_nStinger;
	//////////////////////////////////////////////////////////////////////////////////////////////////////
	// Function: “CGameOverState”
	//
	// Purpose: The default constructor for CGameOverState. Will initialize values.
	//////////////////////////////////////////////////////////////////////////////////////////////////////
	CGameOverState(void);
		
	//////////////////////////////////////////////////////////////////////////////////////////////////////
	// Function: “~CGameOverState”
	//
	// Purpose: The destructor for CGameOverState. Will cleanup.
	//////////////////////////////////////////////////////////////////////////////////////////////////////
	~CGameOverState(void);
		
	//////////////////////////////////////////////////////////////////////////////////////////////////////
	// Function: “CGameOverState”
	//
	// Purpose: The copy constructor for CGameOverState.
	//////////////////////////////////////////////////////////////////////////////////////////////////////
	CGameOverState(const CGameOverState&);
	
	//////////////////////////////////////////////////////////////////////////////////////////////////////
	// Function: “CGameOverState& operator=”
	//
	// Purpose: The assignment operator for CGameOverState.
	//////////////////////////////////////////////////////////////////////////////////////////////////////
	CGameOverState& operator=(const CGameOverState&);

	static CGameOverState* sm_pGameOverStateInstance;
public:
	
	//////////////////////////////////////////////////////////////////////////////////////////////////////
	// Function: “Enter”
	//
	// Purpose: Enter essentially does what the default constructor will do, however it doesn't "need" to
	//			initialize all values. Enter only has to initialize the values that reset everytime the
	//			user enters this state.
	//////////////////////////////////////////////////////////////////////////////////////////////////////
	void	Enter(void);
		
	//////////////////////////////////////////////////////////////////////////////////////////////////////
	// Function: “Input”
	//
	// Purpose: Reads the current input of the user and performs actions based on whether what was input
	//			was being looked for or not.
	//			RETURN - true for this state, at all times
	//////////////////////////////////////////////////////////////////////////////////////////////////////
	bool	Input(void);
		
	//////////////////////////////////////////////////////////////////////////////////////////////////////
	// Function: “Update”
	//
	// Purpose: Update takes care of any increment over time variables as well as anything that constantly
	//			needs to be recalculated.
	//////////////////////////////////////////////////////////////////////////////////////////////////////
	void	Update(float fElapsedTime);
	
	//////////////////////////////////////////////////////////////////////////////////////////////////////
	// Function: “Render”
	//
	// Purpose: Draws to the screen.
	//////////////////////////////////////////////////////////////////////////////////////////////////////
	void	Render(void);
	
	//////////////////////////////////////////////////////////////////////////////////////////////////////
	// Function: “Exit”
	//
	// Purpose: Cleans up any pointers or memory as the state exits.
	//////////////////////////////////////////////////////////////////////////////////////////////////////
	void	Exit(void);

	//////////////////////////////////////////////////////////////////////////////////////////////////////
	// Function: “GetInstance”
	//
	// Purpose: Will return a pointer to the instance of this state and will also initialize the state if
	//			it has not already been initialized.
	//////////////////////////////////////////////////////////////////////////////////////////////////////
	static CGameOverState*	GetInstance(void);
	
	//////////////////////////////////////////////////////////////////////////////////////////////////////
	// Function: “DeleteInstance”
	//
	// Purpose: Will remove the instance of the state from memory.
	//////////////////////////////////////////////////////////////////////////////////////////////////////
	static void DeleteInstance(void);
};

#endif